import CoreGraphics

/// Default line width constants across the application
public enum LineWidth {
    /// Extra extra narrow line width (0.5 pt)
    public static let xxs: CGFloat = 0.5
    /// Extra narrow line width (1.0 pt)
    public static let xs: CGFloat = 1.0
    /// Narrow line width (2.0 pt)
    public static let s: CGFloat = 2.0
    /// Medium line width (3.0 pt)
    public static let m: CGFloat = 3.0
    /// Bold line width (4.0 pt)
    public static let l: CGFloat = 4.0
    /// Extra bold line width (6.0 pt)
    public static let xl: CGFloat = 6.0
}
