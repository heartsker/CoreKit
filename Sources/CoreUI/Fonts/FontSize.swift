import CoreGraphics

/// Default font size constants across the application
public enum FontSize: CGFloat {
    /// Extra extra small font size (10 pt)
    case xxs = 11
    /// Extra small font size (13 pt)
    case xs = 13
    /// Small font size (15 pt)
    case s = 15
    /// Medium font size (17 pt)
    case m = 17
    /// Large font size (28 pt)
    case l = 28
    /// Extra large font size (34 pt)
    case xl = 34
    /// Extra extra large font size (45 pt)
    case xxl = 45
    /// Extra extra extra large font size (73 pt)
    case xxxl = 73
    /// Extra 4x large font size (118 pt)
    case x4l = 118
}
