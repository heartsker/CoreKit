import CoreGraphics

/// Default font size constants across the application
public enum FontSize: CGFloat, Sendable {
    /// Extra extra small font size (10 pt)
    case xxs = 11
    /// Extra small font size (13 pt)
    case xs = 13
    /// Small font size (15 pt)
    case s = 15
    /// Medium font size (17 pt)
    case m = 17
    /// Large font size (22 pt)
    case l = 22
    /// Extra large font size (28 pt)
    case xl = 28
    /// Extra extra large font size (34 pt)
    case xxl = 34
}
